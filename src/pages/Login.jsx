import React, { useState } from 'react';
import { auth } from '../lib/firebase';
import { signInWithEmailAndPassword, createUserWithEmailAndPassword, GoogleAuthProvider, signInWithPopup } from 'firebase/auth';

export default function Login({ onLogin, showToast }) {
    const [mode, setMode] = useState('login'); // 'login' | 'register'
    const [email, setEmail] = useState('');
    const [password, setPassword] = useState('');
    const [isLoading, setIsLoading] = useState(false);

    const handleGoogleLogin = async () => {
        setIsLoading(true);
        try {
            const provider = new GoogleAuthProvider();
            await signInWithPopup(auth, provider);
            showToast('Google ile giriş başarılı!', 'success');
            onLogin();
        } catch (error) {
            console.error(error);
            showToast('Google girişi başarısız.', 'error');
        } finally {
            setIsLoading(false);
        }
    };

    const handleSubmit = async (e) => {
        e.preventDefault();
        if (!email || !password) {
            showToast('Lütfen e-posta ve şifre giriniz.', 'error');
            return;
        }

        setIsLoading(true);

        try {
            if (mode === 'login') {
                await signInWithEmailAndPassword(auth, email, password);
                showToast('Giriş başarılı!', 'success');
            } else {
                await createUserWithEmailAndPassword(auth, email, password);
                showToast('Kayıt başarılı! Hoş geldiniz.', 'success');
            }
            onLogin();
        } catch (error) {
            console.error(error);
            let msg = 'Bir hata oluştu.';
            if (error.code === 'auth/invalid-email') msg = 'Geçersiz e-posta adresi.';
            if (error.code === 'auth/user-not-found') msg = 'Kullanıcı bulunamadı.';
            if (error.code === 'auth/wrong-password') msg = 'Hatalı şifre.';
            if (error.code === 'auth/email-already-in-use') msg = 'Bu e-posta zaten kullanımda.';
            if (error.code === 'auth/weak-password') msg = 'Şifre çok zayıf (en az 6 karakter).';
            showToast(msg, 'error');
        } finally {
            setIsLoading(false);
        }
    };

    return (
        <div className="min-h-screen bg-ios-bg flex items-center justify-center p-6">
            <div className="w-full max-w-sm">
                <div className="text-center mb-10">
                    <div className="w-24 h-24 bg-ios-blue text-white rounded-[2rem] mx-auto flex items-center justify-center text-5xl font-bold shadow-xl shadow-blue-200 mb-6">
                        DT
                    </div>
                    <h1 className="text-3xl font-bold text-gray-900 tracking-tight">
                        {mode === 'login' ? 'Hoş Geldiniz' : 'Hesap Oluştur'}
                    </h1>
                    <p className="text-gray-500 mt-2 text-lg">Ders Takip Asistanı</p>
                </div>

                <form onSubmit={handleSubmit} className="space-y-4">
                    <div className="bg-white rounded-2xl shadow-sm border border-gray-200 overflow-hidden">
                        <div className="border-b border-gray-300">
                            <input
                                type="email"
                                value={email}
                                onChange={(e) => setEmail(e.target.value)}
                                placeholder="E-posta"
                                className="w-full px-5 py-4 text-lg focus:outline-none"
                                required
                            />
                        </div>
                        <div>
                            <input
                                type="password"
                                value={password}
                                onChange={(e) => setPassword(e.target.value)}
                                placeholder="Şifre"
                                className="w-full px-5 py-4 text-lg focus:outline-none"
                                required
                            />
                        </div>
                    </div>

                    <button
                        type="submit"
                        disabled={isLoading}
                        className="w-full bg-ios-blue text-white py-4 rounded-xl font-bold text-lg shadow-ios-blue active:scale-95 transition-all disabled:opacity-50"
                    >
                        {isLoading ? (
                            <span className="flex items-center justify-center gap-2">
                                <span className="w-5 h-5 border-2 border-white/30 border-t-white rounded-full animate-spin"></span>
                                İşleniyor...
                            </span>
                        ) : (
                            mode === 'login' ? 'Giriş Yap' : 'Kayıt Ol'
                        )}
                    </button>

                    <div className="relative my-6">
                        <div className="absolute inset-0 flex items-center">
                            <div className="w-full border-t border-gray-300"></div>
                        </div>
                        <div className="relative flex justify-center text-sm">
                            <span className="px-2 bg-ios-bg text-gray-500">veya</span>
                        </div>
                    </div>

                    <button
                        type="button"
                        onClick={handleGoogleLogin}
                        disabled={isLoading}
                        className="w-full bg-white text-gray-700 py-4 rounded-xl font-bold text-lg shadow-sm border border-gray-300 active:scale-95 transition-all flex items-center justify-center gap-2"
                    >
                        {/* Google Icon SVG */}
                        <svg className="w-6 h-6" viewBox="0 0 24 24">
                            <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                            <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                            <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.84z" fill="#FBBC05" />
                            <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
                        </svg>
                        Google ile Devam Et
                    </button>
                </form>

                <div className="mt-8 text-center">
                    <p className="text-gray-500">
                        {mode === 'login' ? 'Hesabınız yok mu?' : 'Zaten hesabınız var mı?'}
                        <button
                            onClick={() => setMode(mode === 'login' ? 'register' : 'login')}
                            className="text-ios-blue font-semibold ml-2 hover:underline"
                        >
                            {mode === 'login' ? 'Kayıt Olun' : 'Giriş Yapın'}
                        </button>
                    </p>
                </div>
            </div>
        </div>
    );
}

